package com.test.serialize;

public class Bean4{
	String name;
	int id;
	public Bean4()
	{
		
	}
	public Bean4(String name, int id)
	{
		this.name = name;
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}

}
